struct Point {
    ll x, y;
};

string check(Point x, Point y, Point z) {
    ll val = (y.x-x.x) * (z.y - x.y) - (y.y-x.y) * (z.x-x.x);
    if(val < 0) return "RIGHT\n";
    if(val > 0) return "LEFT\n";
    return "TOUCH\n";
}

