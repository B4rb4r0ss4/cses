ll getVal(PLL ele) {
    ll ans = 0;
    if(ele.fs > ele.se) {
        if(!(ele.fs&1)) {
            ans = ele.fs*ele.fs - ele.se+1;
        } else {
            ans = (ele.fs-1)*(ele.fs-1) + 1 + ele.se - 1;
        
        }
    } else if(ele.se > ele.fs) {
        if(ele.se&1) {
            ans = ele.se*ele.se - ele.fs+1;
        } else {
            ans = (ele.se-1) * (ele.se-1) + 1 + ele.fs -1;
        }
    } else {
        if(ele.se&1) ans = ele.se*ele.se - ele.fs+1;
        else ans = ele.fs*ele.fs - ele.se+1;
    }

    return ans;
}

