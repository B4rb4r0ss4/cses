ll gcd(ll a, ll b) { ll t=0; while(b) {a%=b; t=a; a=b; b=t;} return a;}
ll lcm(ll a, ll b) { return a/gcd(a, b) * b;}

ll expo(ll base, ll po, ll MODU) {
    if(!po) return 1;
    if(po&1) 
        return (base * expo(base, po-1, MODU))%MODU;
    ll res = expo(base, po/2, MODU);
    return (res*res)%MODU;
}

