ll lcm(ll a, ll b) { return a/gcd(a, b) * b;}
const int SIZE = 1e6;
vector<int> adj[SIZE];
int connected[SIZE];
bool dead[SIZE];

void dfs(int v, int repre) {
    connected[v] = repre;
    dead[v] = 1;
    for(auto i: adj[v]) {
        if(!dead[i]) {
            dfs(i, repre);
        }
    }
}

